	public int intMax (int number1, int number2) {
		if (number1 > number2) {
			return number1;
		}
		return number2;
	}
	
	public int intMinimum(int number1, int number2) {
		if (number1 > number2) {
			return number2;
		}
		return number1;
	}
	
